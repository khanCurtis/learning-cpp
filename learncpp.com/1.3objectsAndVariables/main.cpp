#include <iostream>
using namespace std;

int main() {
    cout << 5 << "\n";          // prints the literal number '5' and adds a new line
    cout << -6.7 << "\n";       // prints the literal number '-6.7' and adds a new line
    cout << 'H' << "\n";        // prints the literal character 'H' and adds a new line
    cout << "Hello";            // prints the literal string "Hello"

    int x;                      // defines the variable 'x' as an integer
    double y;                   // defines the variable 'y' as a double
    int a, b;                   // defines two variables 'a' & 'b' as integers

    return 0;                   // indicates program has executed successfully
}